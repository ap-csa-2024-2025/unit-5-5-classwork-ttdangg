public class Point2D
{
    private int x;
    private int y;


    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }



    public void setX(int inputX)
    {
        x = inputX;
    }
    
    public void setY(int inputY)
    {
        y = inputY;
    }

}
